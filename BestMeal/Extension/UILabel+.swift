import UIKit


extension UILabel {
    
    func UILabelExtension(placeNameLabelOnTheList: UILabel) {
        placeNameLabelOnTheList.textColor = .white
        placeNameLabelOnTheList.textAlignment = .center
        placeNameLabelOnTheList.layer.cornerRadius = 10.0
    }
}
